const norm = (s: string): string =>
  s.normalize("NFKD").replace(/[\u0300-\u036f]/g, "").toLowerCase().replace(/[^a-z]/g, "");

export function getNegoReviews(nom: string): NegoReview[] {
  return reviewsByNego[norm(nom)] ?? [];
}

export function getNegoStats(nom: string): { count: number; avg5: number | null } {
  const r = getNegoReviews(nom);
  if (!r.length) return { count: 0, avg5: null };
  const avg10 = r.reduce((s, x) => s + x.note10, 0) / r.length;
  return { count: r.length, avg5: Math.round((avg10 / 2) * 10) / 10 };
}
